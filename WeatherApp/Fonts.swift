import Foundation
import UIKit

extension UIFont {
    class func wth36PtFont() -> UIFont? {
        return UIFont(name: "HelveticaNeue", size: 36.0)
    }
    
    class func wth20PtFont() -> UIFont? {
        return UIFont(name: "HelveticaNeue", size: 20.0)
    }
    
    class func wth18PtFont() -> UIFont? {
        return UIFont(name: "HelveticaNeue", size: 18.0)
    }
    
    class func wth12PtFont() -> UIFont? {
        return UIFont(name: "HelveticaNeue", size: 12.0)
    }
}
